#include "sh21.h"

int 		ft_is_caract_(char *str, char *c)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '|')
		{
			(*c) = '|';
			return (1);
		}
		if (str[i] == ';')
		{
			(*c) = '|';
			return (1);
		}
		i++;
	}
	return (0);
}